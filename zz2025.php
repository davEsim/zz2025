       Letošní ročník se vedle Prahy uskutečnil v 56 českých městech. Vůbec poprvé festival zavítal do Černošic či Humpolce. Každé město má svůj autonomní organizační tým, což dodává Jednomu světu na pestrosti. Lokální publika se kromě samotných projekcí mohou těšit také na debaty s tvůrci*kyněmi či koncerty.
